const admin = require('firebase-admin');
const serviceAccount = require('./whatcolor-d1250-firebase-adminsdk-21r68-16cd91dbbc.json');
const dotenv = require('dotenv');

dotenv.config();

let firebase;
if (admin.apps.length === 0) {
  firebase = admin.initializeApp({
    credential: admin.credential.cert(serviceAccount),
  });
} else {
  firebase = admin.app();
}

module.exports = {
  api: require('./api'),
};
